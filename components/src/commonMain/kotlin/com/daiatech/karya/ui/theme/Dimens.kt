import androidx.compose.runtime.Composable
import androidx.compose.ui.unit.dp

/**
 * Standard on-screen dimensions which scale depending on screen density
 */
object Dimens {
    val ZERO @Composable get() = 0.dp
    val XXS @Composable get() = 2.dp
    val XS @Composable get() = 4.dp
    val S @Composable get() = 8.dp
    val M @Composable get() = 16.dp
    val L @Composable get() = 24.dp
    val XL @Composable get() = 32.dp
    val XXL @Composable get() = 48.dp
    val XXXL @Composable get() = 64.dp
}
